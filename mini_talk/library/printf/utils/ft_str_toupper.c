#include "../headers/ft_printf_utils.h"

void	ft_str_toupper(char *str)
{
	char	*p;

	p = str;
	while (p && *p)
	{
		if ('a' <= *p && *p <= 'z')
			*p -= ('a' - 'A');
		p++;
	}
}
